
public class Number {

	public static void main(String[] args) {
		// 더하기, 빼기, 곱하기, 나누기 같은 것들 연산자, Operator라고 부른다.
		System.out.println(6 + 2);	// 8, 더하기 연산
		System.out.println(6 - 2);	// 4, 빼기 연산
		System.out.println(6 * 2);	// 12, 곱하기 연산
		System.out.println(6 / 2);	// 3, 나누기 연산
		
		System.out.println(Math.PI);	// 3.141592653589793, Math라는 그룹 안에 PI라는 명령 호출
		System.out.println(Math.floor(Math.PI));	// 3.0, Math라는 그룹 안에 floor라는 반내림 명령 호출
		System.out.println(Math.ceil(Math.PI));	// 4.0, Math라는 그룹 안에 ceil이라는 반올림 명령 호출
	}

}
// 숫자와 관련된 연산자 및 클래스 사용하여 숫자를 연산