
public class StringOperation {

	public static void main(String[] args) {
		
		System.out.println("Hello World".length());	// 11, length()라는 명령을 통해 문자열의 길이를 알 수 있다.
		System.out.println("Hello, [[[name]]] ... bye.".replace("[[[name]]]", "egoing"));	// replace() 명령을 통해 바꾸고 싶은 문자를 원하는 문자로 바꿀 수 있다.
	}

}

// 문자열이라는 데이터 타입을 표현할 수 있게 되면, 문자열과 관련된 자바에 내장되어있는 여러가지 문자를 처리하는 굉장히 기능이 좋은 것들을 이용해서 문자와 관련된 여러가지 문자를 해결할 수 있다.